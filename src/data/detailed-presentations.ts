// Проверяем, просит ли клиент подробности
export function isAskingForDetails(message: string): boolean {
  const lower = message.toLowerCase();
  const detailPhrases = [
    'подробн',
    'расскаж',
    'первый',
    'второй',
    'третий',
    '1',
    '2',
    '3',
    'фундамент',
    'днк',
    '2.0',
    'коротк'
  ];

  return detailPhrases.some(phrase => lower.includes(phrase)) && message.length < 50;
}
